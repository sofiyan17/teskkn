              Sejarah Desa Kalijaga
            </h2>
            <p class="about__description">
              Desa Kalijaga adalah salah satu Desa tua dari beberapa desa tua yang ada di Pulau Lombok yang pada umumnya sejarah keberadaan Desa â€“ desa tersebut lahir tanpa didukung oleh kronologis kejadian yang tepat. 
            </p>

            <p class="about__description">Sejarah kelahiran Desa Kalijaga diangkat dari penuturan generasi ke generasi secara turun temurun dengan mengambil indikasi perjalanan sejarah Pemerintahan Raja Selaparang yang menyerbu ke beberapa Daerah seperti  Desa Sembalun, Bayan, Wanasaba, Lenek, Pengadangan, Peneda gandor, dan Kopang Benoa.</p>

            <p class="about__description">Diperkirakan pada masa pengembangan ajaran Agama Islam di Jawa oleh para Wali Songo, datanglah seorang penyebar ajaran Agama Islam bernama Raden Rahmat yang konon Raden Rahmat mendarat di Daerah Bintaro (Ampenan), di Daerah Bintaro Raden Rahmat tidak banyak menjalankan misinya yang kemudian perjalanan dilanjutkan ke Pesisir utara Pulau Lombok yaitu Daerah Bayan dan barulah kegiatan penyampaian misi ajaran Agama Islam itu dapat dilaksanakan kepada penduduk setempat dengan cukup lama, sehingga dapat merubah tata cara hidup bermasyarakat, beragama, berbudaya, pola hidup dan penghidupan penduduk Pribumi.</p>

            <p class="about__description">Misi Raden Rahmat terhadap Penduduk Pribumi Bayan mendapat tanggapan positif dan komunikasi sosial Raden Rahmat dengan Penduduk Pribumi berjalan dengan baik, sehingga pengembangan misinya ke Daerah â€“ daerah yang baru selalu didampingi oleh para pengikutnya yang berasal dari Bayan.</p>

            <p class="about__description">Dari Bayan Raden Rahmat melanjutkan perjalanannya menelusuri pesisir Selatan bagian timur Pulau Lombok dan memasuki Daerah Pedalaman yang kemudian diperkirakan bahwa Raden Rahmat sempat  tinggal di Kerajaan Selaparang, yang konon dari Selaparang perjalanan dilanjutkan ke Desa - desa Pedalaman Lombok, salah satu diantaranya yaitu Desa Kalijaga dan selanjutnya ke Desa Mamben dan yang terakhir ke Desa Kalijaga lama.</p>

            <p class="about__description">Kalijaga lama adalah pusat Desanya diperkirakan disekitar Iser - iser (Nama Daerah Aikmel dulu) dan Raden Rahmat banyak mengembangkan pelajaran agama Islam pada penduduk pribumi yang kemudian pelajaran - pelajaran tersebut oleh Masyarakat setempat dijadikan modal untuk mengembangkan tata kehidupan, dan penghidupan dimasa berikutnya dan sejalan dengan perkembangan penduduk yang diimbangi oleh tata kehidupan, agama dan sosial budaya penduduk Desa Kalijaga lama, maka tata pemukiman berkembang ke arah selatan yaitu dari Iser - iser berkembang ke Gubuk Dapur dan Karang Mantri.</p>

            <p class="about__description">Gubuk Dapur dan Karang Mantri adalah merupakan tonggak dimulainya keberadaan Desa Kalijaga setelah dibekali dengan ajaran - ajaran Islam oleh Raden Rahmat sehingga keberadaan beliau ditengah - tengah masyarakat pribumi Kalijaga lama dijadikan sebagai indikasi terhadap kejadian dan asal usul nama Desa Kalijaga yang kita temukan saat ini.</p>

            <p class="about__description">Menurut penuturan dari generasi kegenerasi bahwa asal usul nama Kalijaga itu terdapat beberapa versi dan penapsiran, tetapi semua versi dan penapsiran itu adalah merupakan indikasi /jalan utama untuk menggali Pra Desa Kalijaga. Adapun versi - versi yang berkembang dan hidup itu adalah sebagai berikut :</p>

            <ol>
              <li>Nama Kalijaga berasal dari nama seorang penduduk asli yang berprofesi sebagai seorang petani  yaitu <b>Loq Kalija</b> yang menerima pesan dari Raden Rahmat untuk <b>menjaga</b> Desa tersebut  sehingga nama Loq Kalija menjadi asal usul nama Kalijaga.</li>
              <li>Indikasi yang lain dapat merupakan tradisi khas masyarakat Desa Kalijaga adalah <b>Nasi Sunan</b>  yaitu satu-satunya jenis makanan tradisional yang hanya terdapat di Desa Kalijaga. Nasi Sunan ini dibuat dan disajikan pada peringatan Maulid Nabi Besar Muhammad SAW dan Nasi Sunan tersebut sama seperti Nasi Sunan yang ada di Daerah Jawa Tengah yaitu di Yogyakarta dan konon hidangan ini dihidangkan khusus untuk para Wali Songo dan salah satu Wali Songo yang pernah ke Kalijaga adalah murid <b>Sunan Kalijaga</b> yaitu Sunan Prapen yang membawa tradisi Nasi Sunan tersebut sehingga dari situ kemudian diambil nama Desa Kalijaga.</li>
              <li>Dapat juga nama Kalijaga ini diambil dari posisi Desa Kalijaga yang diapit oleh dua buah <b>sungai ( Kali )</b> sehingga penduduk Desa Kalijaga lama disebut sebagai penduduk  sungai atau penduduk <b>penjaga sungai ( Jaga Kali )</b> yang kemudian menjadi <b>Kalijaga</b>. </li>
            </ol>

